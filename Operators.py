# Operators 

# Intergers and floats (numbers with decimal point)

print(10 + 3)
print(10 - 3)

# Get float with this division -- must not work with Python 2.8, Python 3+ addition? 
print(10 / 3) 

# // division get interger 
print(10 // 3) 

# Modulus operator returns remainder of division 
print(10 % 3) 

# Power - 10 to the power of 3 
print(10 ** 3) 

# Augmented assignment operator 
# Want to increment 10 by 3 in this example 

x = 10 
x = x + 3
print(x) 

# The augmented assignment operator is +=  Can do this with other operators (e.g. -=)
x += 3
print(x)

# Order of operations holds true. 3 * 2 operated first  
x = 10 + 3 * 2
print(x) 

